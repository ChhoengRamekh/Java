package Activity_inheritance;

public class Person {
	public String name;
	private int age;
	
	public Person() {
		
	}
	
	public Person(String name) {
		
	}
	
	public Person(String name, int age) {
		
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	@Override
	public String toString() {
		return super.toString();
	}
	
	
}
